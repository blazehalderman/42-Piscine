char	*ft_rot42(char	*str)
{
	int i;
	char *alpha;
	int count;

	count = 0;
	i = 0;
	while(alpha[i] != '\0' && count != 42)
	{
		if (count != 42)
		{
			i++;
			count++;
		}
		else if (i > 25)
		{
			i = 0;
		}
	}
}
